package com.java.EduCore.dto;

public class InstructorInfoDTO {
    private Long id;
    private String name;
    private String email;

    public InstructorInfoDTO() {
    }

    public InstructorInfoDTO(Long id, String email, String name) {
        this.id = id;
        this.email = email;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
